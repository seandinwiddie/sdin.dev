const xmlEscape = (value) => String(value)
  .replaceAll('&', '&amp;')
  .replaceAll('<', '&lt;')
  .replaceAll('>', '&gt;')
  .replaceAll('"', '&quot;')
  .replaceAll("'", '&apos;');

const gitOutput = (args) => {
  try {
    return execFileSync('git', args, { cwd: root, encoding: 'utf8' }).trim();
  } catch {
    return '';
  }
};

const lastModified = (source) => {
  const dirty = gitOutput(['status', '--porcelain', '--', source]);
  const committed = dirty ? '' : gitOutput(['log', '-1', '--format=%cs', '--', source]);
  return committed || new Date().toISOString().slice(0, 10);
};

const hydratePage = (page) => Object.freeze({
  ...page,
  url: `${siteUrl}${page.route}`,
  imageUrl: `${siteUrl}/og.png`,
  lastmod: lastModified(page.source),
});

const renderUrl = ({ url, imageUrl, title, caption, lastmod }) => `\t<url>
\t\t<loc>${xmlEscape(url)}</loc>
\t\t<lastmod>${xmlEscape(lastmod)}</lastmod>
\t\t<image:image>
\t\t\t<image:loc>${xmlEscape(imageUrl)}</image:loc>
\t\t\t<image:title>${xmlEscape(title)}</image:title>
\t\t\t<image:caption>${xmlEscape(caption)}</image:caption>
\t\t</image:image>
\t</url>`;

const renderUrlSet = (pages) => `<?xml version="1.0" encoding="UTF-8"?>
<?xml-stylesheet type="text/xsl" href="/main-sitemap.xsl"?>
<urlset xmlns="http://www.sitemaps.org/schemas/sitemap/0.9" xmlns:image="http://www.google.com/schemas/sitemap-image/1.1">
${pages.map(renderUrl).join('\n')}
</urlset>
<!-- Generated by scripts/generate-sitemaps.mjs. Do not edit by hand. -->
`;

const renderIndex = (pages) => {
  const latest = pages
    .map(({ lastmod }) => lastmod)
    .sort((left, right) => right.localeCompare(left))[0];

  return `<?xml version="1.0" encoding="UTF-8"?>
<?xml-stylesheet type="text/xsl" href="/main-sitemap.xsl"?>
<sitemapindex xmlns="http://www.sitemaps.org/schemas/sitemap/0.9">
\t<sitemap>
\t\t<loc>${xmlEscape(`${siteUrl}/page-sitemap.xml`)}</loc>
\t\t<lastmod>${xmlEscape(latest)}</lastmod>
\t</sitemap>
</sitemapindex>
<!-- Generated by scripts/generate-sitemaps.mjs. Do not edit by hand. -->
`;
};

const writeIfChanged = (filename, contents) => {
  const destination = resolve(root, filename);
  let current = '';
  try {
    current = readFileSync(destination, 'utf8');
  } catch {
    // A missing generated file is expected on a clean bootstrap.
  }
  if (current !== contents) writeFileSync(destination, contents);
};

const pages = pageEntities.map(hydratePage);
writeIfChanged('page-sitemap.xml', renderUrlSet(pages));
writeIfChanged('sitemap_index.xml', renderIndex(pages));
